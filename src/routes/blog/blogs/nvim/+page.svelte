    <h2 class="text-theme-white text-xl md:text-2xl text-left font-semibold">
      1. Unparalleled Efficiency
    </h2>
    <p>
      Neovim's modal editing paradigm is at the heart of its efficiency. This
      means that users navigate and manipulate text using a series of modes,
      each with a distinct purpose. Normal mode, the default mode, is dedicated
      to text navigation and manipulation, while Insert mode allows for text
      input. This separation of tasks eliminates the need to constantly switch
      between different tools or modes, streamlining the editing process.
    </p>
  </div>

  <div class="flex flex-col gap-4">
    <h2 class="text-theme-white text-xl md:text-2xl text-left font-semibold">
      2. Shaping the Editor to Your Workflow
    </h2>
    <p>
      Neovim's true strength lies in its immense customizability. Unlike most
      editors that enforce a predefined layout and behavior, Neovim grants users
      complete control over every aspect of their editing environment. From
      keybindings and color schemes to plugins and extensions, Neovim can be
      molded to fit the individual's workflow, preferences, and coding style.
    </p>
  </div>

  <div class="flex flex-col gap-4">
    <h2 class="text-theme-white text-xl md:text-2xl text-left font-semibold">
      3. Conserving System Resources
    </h2>
    <p>
      Neovim is a lightweight and resource-efficient editor, making it ideal for
      low-powered machines or situations where resources are constrained. In
      contrast, many graphical editors consume significant system resources,
      leading to performance issues on older or resource-limited devices. Plus,
      if you happen to do any kind of server work, vim in general will save you <b
        >A LOT</b
      > of time.
    </p>
  </div>

  <div class="flex flex-col gap-4">
    <h2 class="text-theme-white text-xl md:text-2xl text-left font-semibold">
      4. You learn how IDEs work
    </h2>
    <p>
      Neovim's modal editing approach and extensive configuration options
      present a learning curve that may initially deter some users. However, the
      rewards are well worth the effort. You start to learn how other IDEs work,
      how language servers work, how bash commands work, how the system works.
      When you use other IDEs you don't know understand what is happening under
      the hood. You just expect everything to work. I feel like having a deeper
      understanding of how the tools you use everyday work makes you a better
      developer.
    </p>
  </div>

  <div class="flex flex-col gap-4">
    <h2 class="text-theme-white text-xl md:text-2xl text-left font-semibold">
      5. Endless Possibilities with Plugins and Extensions
    </h2>
    <p>
      Neovim's plugin ecosystem is vast and ever-expanding, offering a vast
      array of plugins and extensions that cater to diverse programming
      languages, tools, and workflows. These plugins seamlessly integrate with
      Neovim's core functionality, extending its capabilities and adapting it to
      specific development needs.
    </p>
